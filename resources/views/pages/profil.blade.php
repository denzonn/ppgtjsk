            <p>Akhirnya dalam Sidang Klasis Makassar XVIII pada tanggal 9-11
              Juli 1979 di Gedung Gereja Labuang Baji, diputuskan bahwa
              Cabang Kebaktian Tello Batua dan Daya Km. 13 (Yonif Linud
