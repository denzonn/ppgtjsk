            <span>1.2. Dari Makassar Utara ke Ujung Pandang Timur</span>
            <p>Pada tanggal 8 Desember 1963, Majelis Gereja Toraja Makassar
              yang berlokasi di Jalan Maros No. 17 Makassar, mengumumkan
              kepada anggota jemaat bahwa Gereja Toraja Makassar akan
              dimekarkan menjadi tiga jemaat dan selanjutnya akan
              dikoordinir oleh sebuah badan yang disebut Komisi Usaha Klasis
              Makassar.<sup>20</sup> Keputusan untuk memekarkan Gereja Toraja
              Makassar menjadi tiga jemaat dilakukan oleh Majelis Gereja
              Toraja Makassar bersama wakil dari Komisi Usaha Gereja Toraja
              (KUGT), karena saat itu belum terbentuk Komisi Usaha Klasis
              Makassar.<sup>21</sup></p>
            <p>Akan tetapi pengumuman itu belum sempat dilaksanakan,
              Majelis Gereja Toraja Makassar kembali menyampaikan
              pengumuman melalui Berita Djemaat pada tanggal 22 Desember
              1963 bahwa pembentukan ketiga jemaat tersebut ditangguhkan.
              Hal itu kemudian menimbulkan reaksi dari anggota jemaat dan
              memberikan pernyataan tertulis yang ditanda-tangani kurang
              lebih 141 anggota jemaat bahwa mereka tidak setuju bila
              pembentukan ketiga jemaat tersebut ditangguhkan. Akhirnya
              pada tanggal 28 Februari 1964 dan tanggal 2 Maret 1964 diadakan
              Rapat Pleno Majelis Gereja Toraja Jemaat Makassar dan
              diputuskan bahwa untuk merealisasikan pembentukan Klasis
              Makassar yang terdiri dari tiga jemaat, maka dibuat batas-batas
              wilayah pelayanan dari ketiga jemaat baru tersebut. Setelah itu
              barulah ditentukan tiga fase untuk merealisasikan keputusan-keputusan yang terkait dengan pemekaran ketiga
              jemaat
              tersebut, sekaligus pembentukan Klasis Makassar. Akhirnya,
              baru pada tanggal 1 Januari 1965, Gereja Toraja Makassar resmi
              dimekarkan menjadi tiga jemaat yaitu Jemaat Makassar Bahagian
              Utara, Jemaat Makassar Bahagian Tengah dan Jemaat Makassar
              Bahagian Selatan.<sup>22</sup> Sejak saat itu, gedung gereja di Asrama
              Batalyon 700/RIT merasakan sentuhan pelayanan Gereja Toraja
              Jemaat Makassar Bahagian Utara.</p>
            <p>Kurang lebih delapan tahun kemudian, Jemaat Makassar
              Bahagian Utara yang berpusat di Bontoala berubah nama
              menjadi Jemaat Ujung Pandang Utara. Perubahan tersebut sesuai
              keputusan dalam Sidang Klasis Makassar ke XIV di Gedung
              Gereja Jemaat Makassar Bagian Tengah di Jalan Maros pada
              tanggal 16-17 April 1073.<sup>23</sup> Perubahan nama jemaat menjadi
              Jemaat Ujung Pandang Utara untuk menyesuaikan dengan
              perubahan nama kota Makassar menjadi Ujung Pandang pada
              era kepemimpinan Walikota Makassar, H.M. Daeng Patompo
              berdasarkan Peraturan Pemerintah No. 51 tahun 1971. Dalam
              kurun waktu 13 tahun, pelayanan Jemaat Ujung Pandang Utara
              berkembang dengan pesat. Pada tahun 1978, Jemaat Ujung
              Pandang Utara telah memiliki sembilan Cabang Kebaktian
              termasuk Cabang Kebaktian Daya Km 13. Padahal saat itu Jemaat
              Ujung Pandang Utara hanya dilayani oleh dua orang pendeta yaitu Pdt. D.P. Gasong dan Pdt. Benyamin.
              Abubakar.<sup>24</sup> Sesekali
              pelayanan di Jemaat Ujung Pandang Utara saat itu dibantu oleh
              Pdt. Chr. Soetrisno Laban dari Gereja Kristen Jawa Jemaat
              Margoyudan Solo, yang saat itu sedang ditugaskan oleh
              jemaatnya untuk melayani di Daerah VII TNI Angkatan Laut.<sup>25</sup>
              Kesembilan Cabang Kebaktian Jemaat Ujung Pandang Utara itu
              adalah :</p>
            <ul>
              <li>Cabang Kebaktian Kompleks ALRI Daeral VII</li>
              <li>Cabang Kebaktian Wisma Rama</li>
              <li>Cabang Kebaktian Aspol Tallo Lama</li>
              <li>Cabang Kebaktian Rappo Jawa</li>
