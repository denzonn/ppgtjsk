              jumlah anggota Jemaat Biringkanaya saat itu sudah mencapai
              1651 KK atau 8079 jiwa, maupun dari segi wilayah pelayanan
              yang amat luas, dipandang kurang efisien dan efektif apabila
              tetap dipertahankan sebagai satu jemaat. Kedua, wacana
