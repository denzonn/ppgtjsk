            <span>1.3. Dari Ujung Pandang Timur ke Biringkanaya</span>
            <p>Pada awal tahun 1981, Komisi Usaha Klasis Makassar mulai
              menentukan batas-batas wilayah pelayanan bagi jemaat-jemaat
              Gereja Toraja di Kotamadya Ujung Pandang, yaitu Jemaat Ujung
              Pandang Timur, Jemaat Ujung Pandang Utara, Jemaat Ujung
              Pandang Tengah, Jemaat Bunturannu, Jemaat Tamalate, Jemaat
              Labuang Baji dan Jemaat Dadi. Untuk Jemaat Ujung Pandang
              Timur, memiliki wilayah pelayanan mulai dari jembatan Km. 4
              Panaikang ke jurusan Timur sampai ke perbatasan kota Ujung
              Pandang. Batas bagian Baratnya, mulai dari jembatan Km. 4
              Panaikang terus ke Utara sampai ke pinggir laut. Sedangkan
              batas Selatannya menyusuri sungai sampai ke batas wilayah
              pelayanan Jemaat Dadi, kemudian lurus ke Timur sampai
              perbatasan kota.<sup>27</sup> Meskipun wilayah pelayanannya cukup luas,
              Jemaat Ujung Pandang Timur hanya memiliki dua Sektor
              Pelayanan yakni Tello dan Daya, serta menyusul di belakang
              yaitu Kampung Rama.</p>
            <p>Pendeta Jemaat Ujung Pandang Timur yang pertama adalah Pdt.
              Benyamin Abu Bakar yang melayani sejak berdirinya jemaat
              tersebut. Menyusul kemudian Josias Nari diurapi sebagai
              Pendeta Jemaat Ujung Pandang Timur pada tanggal 12 April
              1981. Pada saat diresmikan menjadi jemaat, jumlah anggota
              Jemaat Ujung Pandang Timur baru 590 KK atau 2993 jiwa. Tetapi
              pada tahun 1982, jumlah anggota jemaat meningkat menjadi 631
              KK atau 3961 jiwa. Khusus untuk jumlah anggota Jemaat Ujung Pandang Timur di Sektor Pelayanan Dana baru
              125 KK atau 779
              jiwa.<sup>28</sup></p>
            <p>Dalam kurun waktu kurang dari satu dekade, perkembangan
              anggota Jemaat Ujung Pandang Timur berkembang cukup pesat,
              sehingga muncul keinginan untuk melakukan pemekaran Jemaat
              Ujung Pandang Timur menjadi tiga jemaat. Tim Visitasi Klasis
              Makassar kemudian melakukan visitasi pada tanggal 15
              November 1989 hingga tanggal 19 Januari 1990 di Daya, Tello
              Batua dan Rama. Jumlah anggota Jemaat Ujung Pandang Timur
              di Daya saat itu 394 KK, di Tello Batua 545 KK dan di Rama 418
              KK.<sup>29</sup></p>
            <p>Pada tanggal 27 Maret 1990 diadakan Persidangan Klasis
              Makassar XXVIII atau yang disebut ‘Sidang Klasis Sehari’ di
              Gedung Gereja Tello Batua.<sup>30</sup> Dalam persidangan tersebut
              diputuskan untuk memekarkan Jemaat Ujung Pandang Timur
              menjadi tiga Jemaat yaitu Jemaat Tello Batua, Jemaat Rama dan
              Jemaat Biringkanaya.
              <sup>31</sup> Sesuai hasil Keputusan Persidangan
              Klasis Makassar XXVIII tersebut ketiga jemaat tersebut dinyatakan berdiri sendiri sebagai jemaat mulai
              tanggal 1 April
              1990.<sup>32</sup>
            </p>
            <p>Saat itu Jemaat Biringkanaya dilayani oleh Pdt. Yosias Nari.
              Berhubung Jemaat Biringkanaya belum memiliki tempat ibadah
              sendiri, maka Gedung Gereja Linud 700 BS masih dijadikan
              sebagai pusat pelayanan, sekaligus kantor gereja. Wilayah
              pelayanan Jemaat Biringkanaya ketika itu masih cukup luas,
              karena membentang mulai dari Jembatan Tello sampai ke
              Wilayah Pepabri Sudiang Km. 18 Makassar. Itulah sebabnya,
              Jemaat Biringkanaya terdiri dari empat Tempat kebaktian yaitu
              SMA Soleman, Yonkav, Linud 700 BS, dan Pepabri Sudiang
              Permai. Dibangunnya Perumahan BTN Pepabri Sudiang sejak
              tahun 1990, kompleks perumahan Bumi Tamalanrea Permai
              (BTP) pada tahun 1992, Perumahan Telkomas pada tahun 1996,
              serta beberapa perumahan baru di Sudiang pada tahun 1996,
              menjadi salah satu faktor yang membuat jumlah anggota di
              Jemaat Biringkanaya tumbuh dengan pesat. Akibat pesatnya
              pertambahan jumlah anggota jemaat pada saat itu, maka
              beberapa Tempat Kebaktian ikut dibuka yaitu BTN Angkatan
              Laut, Paccerakkang, Mandai dan Sudiang Raya. Pada tahun 1998
              dibuka Tempat Kebaktian di Kodam Citra Daya dan kemudian
              tahun 2000, dibuka di Kompleks Balla’ Tamalanrea.</p>
            <p>Pada tahun 1997, muncul usulan agar Cabang Kebaktian
