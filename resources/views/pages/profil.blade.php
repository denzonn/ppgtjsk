              Gedung Gereja Yonif 700/Raider, tetapi pindah ke gedung gereja
              yang sedang dibangun di sekitar Perumahan Nusa Tamalanrea
              Indah (NTI), Bontoramba.</p>
            <p>Kejadian ini membawa hikmah tersendiri dan menjadi salah satu
              alasan kuat bagi Majelis Jemaat Biringkanaya untuk
              mempercepat proses pembangunan gedung gereja sendiri di
              NTI, Bontoramba. Proses pembangunan gedung gereja di NTI,
              selama beberapa tahun terkatung-katung karena sulitnya
              mendapatkan IMB akibat adanya resistensi dari FPI dan
              beberapa kelompok sejenis FPI.<sup>14</sup></p>
            <p>Ketika Komandan Yonif 700/Raider dijabat oleh Letkol Inf.
              Febriel Buyung Sikumbang, SH, barulah proses renovasi bisa
