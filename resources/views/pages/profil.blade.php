              Kepanitiaan tersebut dipimpin langsung oleh Letkol Inf. Febriel
              Buyung Sikumbang, SH. sebagai ketua panitia dan wakil ketua
              panitia Ir. Pither Patawaran mewakili Jemaat Biringkanaya.</p>
            <p>Setelah mendapatkan persetujuan Panglima Kodam VII
              Wirabuana dan izin dari Kepala Staf Angkatan Darat, renovasi
              gedung gereja dimulai dan ditandai dengan ibadah dan
