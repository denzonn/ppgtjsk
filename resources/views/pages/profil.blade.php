            <span>1.1. Gereja Bambu Prajurit Eks Yonif 758</span>
            <p>Lahirnya Jemaat Satria Kasih tidak dapat dipisahkan dari sebuah
              gedung gereja yang kini bernama Gedung Gereja Satria Kasih.
              Sebelum dinamakan Satria Kasih, gedung gereja tersebut telah
              diputuskan menjadi wilayah pelayanan Gereja Toraja Jemaat
              Makassar Utara pada tanggal 21 Agustus 1964 dalam rapat
              majelis Gereja Toraja Makassar.<sup>1</sup> Keputusan itu diambil
              bersamaan dengan terbentuknya Komisi Usaha Klasis (KUK)
              Makassar periode pertama, sekaligus pembentukan susunan
              pengurusnya.<sup>2</sup></p>
            <p>Dalam rapat tersebut, ada tiga keputusan penting terkait
              pembagian wilayah pelayanan tiga jemaat yang baru dimekarkan yaitu Jemaat Makassar Bahagian Utara, Jemaat
              Makassar
              Bahagian Timur dan Jemaat Makassar Bahagian Selatan, serta
              pembagian tugas pelayanan bagi tiga orang pendeta yaitu Pdt.
              B.D. Bijang, S.Th, Pdt. J. Matana dan Pdt. D. Tanga. Salah satu
              keputusannya adalah bahwa Cabang Kebaktian di Batua (Tello)
              dan Daya, Asrama Batalyon 700/RIT dinyatakan termasuk dalam
              wilayah pelayanan Jemaat Makassar Bahagian Utara dengan
              gembalanya adalah Pdt. B.D. Bijang, S.Th.</p>
            <p>Bila ditelisik lebih jauh, ternyata gedung gereja tersebut memiliki
              kisah yang cukup panjang dan tidak dapat dipisahkan dari
              keberadaan Batalyon Infanteri 758, yang sebelumnya bernama
              Batalyon R dan bermarkas di Palu. Kabarnya, sekitar 90%
              pasukan Batalyon R adalah orang Toraja dan dipimpin oleh
              Kapten Inf. Mesach Frans Karangan, sehingga Batalyon R sering
              diplesetkan dengan nama ‘Batalyon Rantepao’. Batalyon R
              dikukuhkan menjadi Batalyon Infanteri (Yonif) 758 pada tanggal
              20 November 1958, sebelum ditugaskan ke Sulawesi Utara untuk
              menghadapi pemberontakan Permesta.<sup>3</sup></p>
            <p>Pada tanggal 5 Januari 1961, Yonif 758 dipindah-tugaskan ke
              Rantepao, sesuai Surat Perintah Dejahit No.Sp-0235/1/1961.
              Panglima KOANDA-IT sebenarnya masih menginginkan agar
              Yonif 758 tetap bertugas di Palu, tetapi Panglima Kodam XIV
              Hasanuddin, Kol. M. Jusuf bersikeras agar Yonif 758 ditarik dari
              Palu ke Rantepao guna membantu menumpas gerombolan
              DI/TII. Saat pindah ke Rantepao, Komandan Yonif 758, Kapten
              Frans Karangan juga menyandang posisi terhormat sebagai
              anggota Majelis Permusyawaratan Rakyat Sementara (MPRS)
              periode 1960-1965. Meskipun telah menjadi anggota MPRS,
              Kapten Inf. Frans Karangan masih tetap mendapat kepercayaan untuk memimpin pasukannya untuk mengamankan
              beberapa
              tempat dari gangguan gerombolan DI/TII. <sup>4</sup></p>
            <p>Keberhasilan Yonif 758 dalam beberapa penugasan, menjadi
              alasan kuat bagi Kol. M. Jusuf untuk memberi kesempatan bagi
              seluruh anggota Yonif 758 untuk mengikuti pendidikan raider di
              Depot Mawang. Suksesnya operasi militer saat menumpas
              pemberontakan PRRI/Permesta menjadi salah satu alasan
              Pemerintah untuk memutuskan bahwa Indonesia Timur
              membutuhkan satu batalyon pemukul atau batalyon raider yang
              dapat dimobilisasi melalui udara. Pilihan Panglima Jusuf jatuh
              kepada Yonif 758.<sup>5</sup></p>
            <p>Dengan adanya kebijakan tersebut, prajurit Yonif 758 yang
              sebelumnya bermarkas di Rantepao akhirnya pindah ke
              Makassar dan bermarkas di Daya. Penempatan asrama sekaligus
              markas batalyon pemukul di Daya merupakan salah satu strategi
              Letkol M. Yusuf untuk mengamankan Kota Makassar dari
              gangguan keamanan yang kerap dilakukan oleh gerombolan
              DI/TII di sekitar Daya, terutama menghambat pergerakan
              Pasukan DI/TII pimpinan Nurdin Piso ke Kota Makassar.<sup>6</sup></p>
            <p>Pada tanggal 10 Juli 1961, Panglima Kodam XIV Hasanuddin
              memerintahkan Kepala Zeni Bangunan Kodam XIV Hasanuddin
              untuk membangun sebuah kompleks asrama tentara yang
              berkekuatan satu batalyon di Jalan Maros Km. 12-13.<sup>7</sup> Saat itu
              tanah yang akan dibangun menjadi asrama Batalyon Raider
              Hasanuddin masih dalam penguasaan PTT Makassar.
              Pembangunan asrama tersebut rampung pada tahun 1962 dan
              kemudian ditempati oleh empat kompi Yonif 758 dibawah
              pimpinan Kapten Inf. Frans Karangan. Sedangkan satu kompi
              Yonif 758 ditempatkan di Jalan Maros Km. 10 bersama Batalyon
              Kavaleri (Yonkav) 10 Serbu.
              Setelah menyelesaikan pendidikan raider di Depot Mawang,
              Yonif 758 berubah menjadi Batalyon Raider Hasanuddin.
              Mesach Frans Karangan masih diberi kepercayaan oleh Panglima
              Kodam IXV Hasanuddin untuk menjadi Komandan Batalyon
              Raider Hasanuddin sampai tahun 1962 dan mendapatkan
              kenaikan pangkat menjadi Mayor Infanteri. Pada saat itu pula,
              APRI berubah menjadi Angkatan Bersenjata Republik Indonesia
              (ABRI).
              Dalam kompleks asrama tersebut, ada sebuah gereja bambu yang
              turut dibangun oleh Zeni Bangunan Kodam XIV Hasanuddin
              pada tahun 1961. Disebut gereja bambu karena dinding, tiang
              dan kursinya terbuat dari bambu. Gereja tersebut sangat
              sederhana karena hanya berlantai tanah dan beratapkan sirap.
              Lokasinya tak jauh dari makam Landorundun di dalam
              Kompleks Asrama Batalyon Raider Hasanuddin.<sup>8</sup></p>
            <p>Beberapa bulan kemudian, Mayor Inf. Frans Karangan ditarik
              menjadi Anggota Asisten III Operasi Mandala dan Kapten Inf.
              Pieter Sumbu diangkat menjadi Komandan Batalyon Raider
              Hasanuddin. Pada era kepemimpinan Kapten Inf. Pieter Sumbu,
              gereja bambu yang semula berlokasi di dekat makam
              Landorundun, dibongkar dan pindahkan ke halaman depan
              menghadap ke Jalan Raya Maros. Kebetulan halaman depan
              kompleks asrama masih kosong dan tidak digunakan, sehingga
              dianggap lebih cocok menjadi lokasi gereja.
              Atap gereja yang tadinya dari sirap diganti dengan atap seng.
              Sedangkan dindingnya diganti dengan papan kayu. Kebetulan
              saat itu, Kapten Inf. Pieter Sumbu diperintahkan untuk
              menduduki dan membersihkan daerah sebelah timur asrama
              yang saat itu masih berupa hutan semak belukar yang masih
              dipenuhi kera dan babi hutan. Daerah itulah yang kerap menjadi
              tempat persembunyian gerombolan DI/TII.<sup>9</sup></p>
            <p>Beberapa pohon jati
              yang banyak tumbuh di sekitar hutan tersebut, kemudian
              ditebang dan digotong ke dalam asrama. Setelah itu diolah
              menjadi papan dan tiang gereja oleh beberapa prajurit yang
              memiliki ketrampilan tukang kayu.
              Pada tanggal 20 Maret 1963, Batalyon Raider Hasanuddin
              berubah nama menjadi Batalyon 700/Raider Indonesia Timur
              (RIT). Dua tahun kemudian gedung gereja tersebut menjadi
              bagian dari pelayanan Gereja Toraja Jemaat Makassar Bagian
              Utara, dimana sebagian besar anggota yang dilayani adalah
              prajurit eks Yonif 758.
              Pada tanggal 28 Maret 1967, kesatuan tempur tersebut berubah
              lagi menjadi Batalyon Infanteri Lintas Udara (Yonif Linud) 700. Itulah sebabnya, gedung gereja tersebut
              lebih dikenal dengan
              nama Gereja Yonif Linud 700, sekalipun batalyon itu kemudian
              mengalami beberapa kali perubahan nama hingga nama yang
              terakhir yakni Batalyon Infanteri Raider 700/Wira Yudha Cakti.
              <sup>10</sup>
              Pada tahun 1981, ketika daerah Daya Km. 13 menjadi Sektor
              Pelayanan dari Jemaat Ujung Pandang Timur, dilakukan
              perbaikan plafon dan emper bagian Timur gedung gereja.<sup>11</sup>
              Kemudian pada tahun 1982, saat Kapten Inf. Thomas Albert
              Umboh menjadi Wakil Komandan Yonif Linud 700, dibangun
              ruang konsistori di bagian belakang gereja.<sup>12</sup> Setelah itu, cukup
              lama Gedung Gereja Linud tidak tersentuh renovasi, namun
              tetap digunakan sebagai tempat beribadah anggota Yonif Linud
              700 dan warga Jemaat Ujung Pandang Timur.
              Setelah daerah Daya Km. 13 dan sekitarnya menjadi bagian dari
              wilayah Pelayanan Jemaat Biringkanaya, sempat ada upaya dari
              Badan Pekerja Majelis Jemaat Biringkanaya untuk melakukan
              renovasi gedung gereja. Tetapi keinginan Majelis Jemaat
              Biringkanaya saat itu ditolak oleh Kodam VII Wirabuana.<sup>13</sup>
            <p>Bahkan pada saat Pdt. Paulus Pasamba menjadi Ketua BPM
              Jemaat Birngkanaya, sempat terjadi kebekuan hubungan antara Yonif Linud 700/WYC -yang saat itu sudah
              berubah nama
              menjadi Yonif 700/Raider- dengan Majelis Jemaat Biringkanaya.
              Hal itu diduga dipicu oleh kebijakan Komandan Yonif
              700/Raider, Letkol Inf. Syaiful Anwar yang melarang adanya
