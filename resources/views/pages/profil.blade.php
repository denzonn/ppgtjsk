              mulai mengumpulkan dana dari persembahan-persembahan
              anggota jemaat dan dari donatur-donatur, baik yang diupayakan
              melalui jejaring Komandan Yonif 700/Raider maupun melalui
              persembahan-persembahan anggota jemaat dan upaya panitia
              dalam Jemaat Biringkanaya. Pada Persidangan Pleno Majelis
              Jemaat Biringkanaya pada tanggal 23 Januari 2012, diputuskan
              untuk memberikan dukungan penuh kepada panitia renovasi
              dan melibatkan seluruh anggota Jemaat Biringkanaya melalui
              pengadaan kartu persembahan bagi setiap anggota kepala
              Keluarga dalam Jemaat Biringkanaya dan dijalankan sepanjang
              tahun 2012.</p>
