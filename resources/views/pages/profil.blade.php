            <p>Jemaat Satria Kasih adalah salah satu jemaat hasil pemekaran
              dari Jemaat Biringkanaya berdasarkan Keputusan Persidangan
              XLIV Klasis Makassar pada tahun 2012. Saat itu Jemaat
              Biringkanaya merupakan jemaat yang memiliki anggota jemaat
              yang paling banyak dalam lingkup Gereja Toraja. Anggota
              Jemaat Biringkanaya tersebar di lima Tempat Kebaktian yakni;
              Angkatan Laut, Kodam Citra, Yonif 700/Raider, Lanraki dan
              Bontoramba. Semua Tempat Kebaktian sedang dalam upaya
              pembangunan fisik dan sebagian sedang berusaha mencari tanah
              untuk tempat pendirian bangunan gedung gereja.</p>
            <p>Pemekaran Jemaat Biringkanaya bermula dari Persidangan Pleno
              Majelis Jemaat Biringkanaya pada tanggal 23-24 Januari 2010
              yang memutuskan bahwa dalam rangka efektifitas pelayanan di
              tengah-tengah jemaat, maka Jemaat Biringkanaya yang
              anggotanya sudah lebih dari 1000 Kepala keluarga perlu
              dimekarkan.<sup>39</sup> Keputusan untuk memekarkan Jemaat Biringkanaya ditindaklanjuti oleh Badan Pekerja
              Majelis Jemaat
              Biringkanaya dengan membentuk Tim Pelaksanaan Studi
              Kelayakan yang diketuai oleh Simon Simi Lopang, SH, MH dan
              didampingi Amir Jaya, SE, M.Si selaku sekretaris.</p>
            <p>Berdasarkan hasil studi kelayakan, tim kemudian
              merekomendasikan lima alternatif Pemekaran Jemaat
              Biringkanaya yang selanjutnya dibahas dalam Sidang Pleno
              Majelis Jemaat Biringkanaya pada bulan Juli 2010. Sidang Pleno
              majelis pada saat itu memutuskan bahwa berdasarkan hasil studi
              kelayakan maka Jemaat Biringkanaya akan dimekarkan menjadi
              lima jemaat. Keputusan tersebut selanjutnya diteruskan ke
              Persidangan XLIII Klasis Makassar di Jemaat Tamalanrea sebagai
              usulan Jemaat Biringkanaya. Usulan tersebut disetujui oleh
              Persidangan Klasis dan memutuskan bahwa menerima usul
              Jemaat Biringkanaya untuk dimekarkan menjadi lima jemaat dan
              menugaskan Badan pekerja Klasis Makassar untuk melakukan
              perlawatan.</p>
